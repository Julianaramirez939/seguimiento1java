public class Ejercicio3x2 {
    public static void main(String[] args) {

        char category = 'X';
        System.out.println("Your category is: " + category);


        char firstSymbol = '@';
        char secondSymbol = '#';
        char nextSymbol = (char) (firstSymbol + 1);
        System.out.println("First symbol: " + firstSymbol);
        System.out.println("Second symbol: " + secondSymbol);
        System.out.println("Next symbol: " + nextSymbol);


        char lineBreak = '\n';
        char horizontalTab = '\t';
        char backslash = '\\';
        System.out.println("This is a new line" + lineBreak + "This is a tab" + horizontalTab + "This is a backslash" + backslash);
    }
}

