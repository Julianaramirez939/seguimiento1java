public class Ejercicio3x5 {
    public static void main(String[] args) {
        int integerValue = 42;
        float floatValue = (float) integerValue;
        System.out.println("Converted float value: " + floatValue);

        float priceFloat = 29.99f;
        int priceInt = (int) priceFloat;
        System.out.println("Converted int price value: " + priceInt);

        char gradeChar = 'B';
        int gradeInt = (int) gradeChar;
        System.out.println("Converted int grade value: " + gradeInt);
    }
}

