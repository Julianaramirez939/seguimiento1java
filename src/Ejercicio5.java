public class Ejercicio5 {
    public static void main(String[] args) {
        int counter = 10;
        int countdown = 5;

        System.out.println("Initial value of the counter: " + counter);
        System.out.println("Initial value of the countdown: " + countdown);

        counter++;
        System.out.println("After incrementing the counter: " + counter);

        countdown--;
        System.out.println("After decrementing the countdown: " + countdown);
    }
}

